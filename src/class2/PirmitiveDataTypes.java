public class PirmitiveDataTypes {

	public static void main(String[] args) {

		// byte

		byte box1 = 5; // -128 to 128

		short box2 = 128; // -32768 to 32768

		int box3 = 1000; // -2147483648 to 2147483648

		long box4 = 100000000000L; // if number is longer than ten digit then we use long type

		float box5 = 5.123456543f; // it will show only six digit after decimal

		double box6 = 6.1234566432; // it will shows ten digit after decimal.

		char box7 = 'A'; // it can only store one charector

		boolean box8 = true; // it will take true or false value
		boolean box9 = false;
		
		// cntr+shift+f  shortcut to organize your code 
		
		

	}

}
