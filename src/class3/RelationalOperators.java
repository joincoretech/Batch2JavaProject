package class3;

public class RelationalOperators {
	
	
	public static void main(String [] args) {
		
		
		// relational opertars are : > ,  < , >=,  <=,  == ,  !=;
		
		int num1 = 10;
		
		int num2 = 5;
		
		System.out.println(num1 > num2); // true
	    System.out.println(num1< num2); // false
	    System.out.println(num1 >= num2); // true
	    System.out.println(num1<= num2); // false
	    System.out.println(num1==num2); // false
	    System.out.println(num1!=num2); // true
	    
	}

}
