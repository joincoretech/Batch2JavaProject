package class6;

public class PrintNumberBiggerToSmaller {
	
	
	public static void main(String [] args) {
		
		// create a program to print number from 10 to 1 use while loop
		
		
		int num=10;
		
		while (num>=1) {
			
			System.out.print(num+" ");
			num--;
		}
		
		// print even number
		
		
		
	}

}
